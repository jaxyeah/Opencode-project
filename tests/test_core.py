class TestTypes:
    def test_connection_config_defaults(self):
        c = ConnectionConfig()
        assert c.opticstudio_path is None
        assert c.headless is True
        assert c.timeout == 60
        assert "auto" in str(c)

    def test_zpl_execution_result_str(self):
        r = ZPLExecutionResult(0, "hello", "", 1.5, "test.zpl")
        assert "OK" in str(r)
        r2 = ZPLExecutionResult(1, "", "err", 0.1, "fail.zpl")
        assert "FAIL" in str(r2)

    def test_zpl_error_str(self):
        e = ZPLError(42, "E_UNDEFINED", "Undefined FOO", "ERROR")
        s = str(e)
        assert "L42" in s
        assert "FOO" in s

    def test_macro_info_defaults(self):
        m = MacroInfo(
            path="/tmp/t.zpl",
            name="t.zpl",
            size=100,
            last_modified=datetime.now(),
        )
        assert m.category == "general"
        assert m.line_count == 0

    def test_regression_test_result_str(self):
        r = RegressionTestResult("t.zpl", True, "a", "a", "")
        assert "PASS" in str(r)
        r2 = RegressionTestResult("f.zpl", False, "a", "b", "diff here")
        assert "FAIL" in str(r2)

    def test_generation_template_str(self):
        t = GenerationTemplate("test", "desc", {"x": None}, "body")
        assert "test" in str(t)


