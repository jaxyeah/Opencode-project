class TestManager:
    def _make_zpl_dir(self, files: dict):
        """Create a temporary directory with .ZPL files from a dict of {name: content}."""
        tmpdir = tempfile.mkdtemp()
        for name, content in files.items():
            path = os.path.join(tmpdir, name)
            with open(path, "w", encoding="utf-8") as f:
                f.write(content)
        return tmpdir

    def test_discover_single(self):
        files = {"test.ZPL": "! test macro\nPRINT 'hello'"}
        d = self._make_zpl_dir(files)
        try:
            manager = ZPLManager()
            macros = manager.discover(d)
            assert len(macros) == 1
            assert macros[0].name == "test.ZPL"
            assert macros[0].line_count == 2
            assert macros[0].size > 0
        finally:
            import shutil
            shutil.rmtree(d, ignore_errors=True)

    def test_discover_multiple(self):
        files = {f"macro_{i}.ZPL": f"! macro {i}" for i in range(5)}
        d = self._make_zpl_dir(files)
        try:
            manager = ZPLManager()
            macros = manager.discover(d, recursive=False)
            assert len(macros) == 5
        finally:
            import shutil
            shutil.rmtree(d, ignore_errors=True)

    def test_discover_nonexistent_dir(self):
        manager = ZPLManager()
        with pytest.raises(FileNotFoundError):
            manager.discover("/nonexistent/path/xyz")

    def test_categorize(self):
        files = {
            "tilt_test.ZPL": "! tilt analysis",
            "distortion_test.ZPL": "! distortion calc",
            "unknown_test.ZPL": "! something",
        }
        d = self._make_zpl_dir(files)
        try:
            manager = ZPLManager()
            macros = manager.discover(d)
            cats = manager.categorize(macros)
            assert "tilt_analysis" in cats
            assert "distortion" in cats
            assert "general" in cats
        finally:
            import shutil
            shutil.rmtree(d, ignore_errors=True)

    def test_search(self):
        files = {
            "HUD_test.ZPL": "! hud analysis",
            "other.ZPL": "! something else",
        }
        d = self._make_zpl_dir(files)
        try:
            manager = ZPLManager()
            results = manager.search(d, "HUD")
            assert len(results) == 1
            assert "HUD" in results[0].name
        finally:
            import shutil
            shutil.rmtree(d, ignore_errors=True)


