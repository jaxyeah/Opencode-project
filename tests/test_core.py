class TestGenerator:
    def test_builtin_templates_loaded(self):
        gen = ZPLGenerator()
        names = [t.name for t in gen.list_templates()]
        assert "tilt-analysis" in names
        assert "distortion" in names
        assert "mtf-analysis" in names

    def test_generate_tilt_analysis(self):
        gen = ZPLGenerator()
        code = gen.generate("tilt-analysis", {"lens_file": "test.zmx", "field_count": 5})
        assert "test.zmx" in code
        assert "FOR i, 1, 5, 1" in code
        assert "NEXT" in code
        assert "Auto-generated by zpl_toolkit" in code

    def test_generate_missing_required_param(self):
        gen = ZPLGenerator()
        with pytest.raises(ValueError, match="lens_file"):
            gen.generate("tilt-analysis", {})

    def test_generate_unknown_template(self):
        gen = ZPLGenerator()
        with pytest.raises(KeyError, match="nonexistent"):
            gen.generate("nonexistent", {})

    def test_save_and_file_exists(self):
        gen = ZPLGenerator()
        with tempfile.NamedTemporaryFile(suffix=".zpl", delete=False) as f:
            path = f.name
        try:
            gen.save("tilt-analysis", {"lens_file": "t.zmx"}, path, force=True)
            with open(path, encoding="utf-8") as f:
                content = f.read()
            assert "t.zmx" in content
        finally:
            os.unlink(path)

    def test_save_file_exists_no_force(self):
        gen = ZPLGenerator()
        with tempfile.NamedTemporaryFile(suffix=".zpl", delete=False) as f:
            path = f.name
        try:
            gen.save("tilt-analysis", {"lens_file": "t.zmx"}, path, force=True)
            with pytest.raises(FileExistsError):
                gen.save("tilt-analysis", {"lens_file": "t.zmx"}, path, force=False)
        finally:
            os.unlink(path)

    def test_default_params_applied(self):
        gen = ZPLGenerator()
        code = gen.generate("distortion", {"lens_file": "test.zmx"})
        assert "test.zmx" in code
        # Default param (reference_wavelength=1) should be substituted
        assert "$" not in code or "PRIMARY $reference_wavelength" not in code


