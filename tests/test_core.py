class TestParser:
    def test_parse_error_line(self):
        parser = ZPLParser()
        result = ZPLExecutionResult(
            1, "", "Error at line 42: Undefined variable FOO", 0.1, "t.zpl"
        )
        parsed = parser.parse(result)
        assert len(parsed.errors) == 1
        assert parsed.errors[0].line_number == 42
        assert "FOO" in parsed.errors[0].message
        assert parsed.errors[0].severity == "ERROR"

    def test_parse_warning_line(self):
        parser = ZPLParser()
        result = ZPLExecutionResult(
            0, "", "Warning: Deprecated function", 0.1, "t.zpl"
        )
        parsed = parser.parse(result)
        assert len(parsed.errors) == 1
        assert parsed.errors[0].severity == "WARNING"

    def test_parse_empty_output(self):
        parser = ZPLParser()
        result = ZPLExecutionResult(0, "", "", 0.1, "t.zpl")
        parsed = parser.parse(result)
        assert len(parsed.errors) == 0
        assert not parsed.has_errors

    def test_parse_unknown_error(self):
        parser = ZPLParser()
        result = ZPLExecutionResult(
            1, "", "Something failed unexpectedly", 0.1, "t.zpl"
        )
        parsed = parser.parse(result)
        assert len(parsed.errors) == 1
        assert parsed.errors[0].error_code == "E_UNKNOWN"

    def test_extract_data_key_value(self):
        parser = ZPLParser()
        result = ZPLExecutionResult(
            0, "Field=0.5\nDistortion=2.34", "", 0.1, "t.zpl"
        )
        parsed = parser.parse(result)
        assert parsed.data == {"Field": 0.5, "Distortion": 2.34}

    def test_parse_multiline_error(self):
        parser = ZPLParser()
        stderr = "Error at line 10: Syntax error\nError at line 20: Type mismatch\n"
        result = ZPLExecutionResult(1, "", stderr, 0.1, "t.zpl")
        parsed = parser.parse(result)
        assert len(parsed.errors) == 2


